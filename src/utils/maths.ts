export const add = (a: number, b: number) => {
  const result = a + b
  return result
}

export const minus = (a: number, b: number) => {
  const result = a - b
  return result
}

export const times = (a: number, b: number) => {
  const result = a * b
  return result
}

export const div = (a: number, b: number) => {
  const result = a / b
  return result
}
